Inner join 
(inner 생략 가능)
양쪽 테이블에 모두 내용이 존재하는 경우 결과 출력

[Left/Right/Full] Outer join 
(outer 생략 가능)
기준 테이블 내용의 누락없이 검색, 대상 테이블의 내용 가져오기
두 가지 테이블 내용을 한번에 출력 가능
Left: 첫 번째 테이블의 내용은, 두 번째 테이블과 연계되는 내용이 없더라도 모두 검색되어야 한다.
Right: 두 번째 테이블의 내용은, 첫 번째 테이블과 연계되는 내용이 없더라도 모두 검색되어야 한다.
Full: 모든 테이블이 모두 검색되어야 한다.

